def filter_long_triangles(
        points: np.ndarray,
        faces: np.ndarray,
        max_edge_percentile: float = 75.0,
        edge_scale: float = 2.0
) -> np.ndarray:
    if len(faces) == 0:
        return faces

    max_edges = []

    for tri in faces:
        p0 = points[tri[0]]
        p1 = points[tri[1]]
        p2 = points[tri[2]]

        e01 = np.linalg.norm(p0 - p1)
        e12 = np.linalg.norm(p1 - p2)
        e20 = np.linalg.norm(p2 - p0)

        max_edges.append(max(e01, e12, e20))

    max_edges = np.array(max_edges)

    base = np.percentile(max_edges, max_edge_percentile)
    threshold = edge_scale * base

    mask = max_edges < threshold
    filtered_faces = faces[mask]

    print(f"Triangle filtering:")
    print(f"  before: {len(faces)}")
    print(f"  after:  {len(filtered_faces)}")
    print(f"  removed: {len(faces) - len(filtered_faces)}")
    print(f"  max edge threshold: {threshold}")

    return filtered_faces

def remove_outliers_mad(points: np.ndarray, threshold: float = 3.5) -> np.ndarray:
    """
    Robust outlier removal using Median Absolute Deviation.

    Видаляє точки, які далеко від медіани по x, y, z.
    Добре працює, коли більшість точок нормальні, а частина — дикі викиди.
    """
    if points.shape[1] != 3:
        raise ValueError("Очікується масив форми Nx3")

    median = np.median(points, axis=0)
    abs_deviation = np.abs(points - median)

    mad = np.median(abs_deviation, axis=0)

    # Захист від ділення на нуль
    mad[mad < 1e-12] = 1e-12

    robust_z_score = 0.6745 * abs_deviation / mad

    mask = np.all(robust_z_score < threshold, axis=1)

    filtered = points[mask]

    print(f"Outlier removal:")
    print(f"  before: {len(points)}")
    print(f"  after:  {len(filtered)}")
    print(f"  removed: {len(points) - len(filtered)}")

    return filtered

