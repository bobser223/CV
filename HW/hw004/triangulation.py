def filter_points_pipeline(
        points: np.ndarray,
        z_min: float = 0.0,
        z_max: float = 3000.0,
        radius_percentile: float = 75.0,
        radius_scale: float = 2.5,
) -> np.ndarray:
    if points.shape[1] != 3:
        raise ValueError("Очікується масив форми Nx3")

    print(f"Initial points: {len(points)}")

    # 1. Тільки скінченні числа
    mask = np.all(np.isfinite(points), axis=1)
    points = points[mask]
    print(f"After finite filter: {len(points)}")

    # 2. Фізичний фільтр по глибині
    mask = (points[:, 2] > z_min) & (points[:, 2] < z_max)
    points = points[mask]
    print(f"After z range [{z_min}, {z_max}]: {len(points)}")

    if len(points) == 0:
        return points

    # 3. Robust фільтр по відстані від медіанного центру
    center = np.median(points, axis=0)
    distances = np.linalg.norm(points - center, axis=1)

    base_radius = np.percentile(distances, radius_percentile)
    max_radius = radius_scale * base_radius

    mask = distances < max_radius
    points = points[mask]

    print(f"After robust radius filter:")
    print(f"  center = {center}")
    print(f"  base_radius = {base_radius}")
    print(f"  max_radius = {max_radius}")
    print(f"  remaining = {len(points)}")

    return points


